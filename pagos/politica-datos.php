    <h1 class="mb-4">Política de Privacidad</h1>
    <p>En <strong>La Rosticería</strong>, valoramos su privacidad y estamos comprometidos a proteger la información personal que comparte con nosotros. Esta Política de Privacidad explica cómo recopilamos, utilizamos y protegemos su información cuando utiliza nuestra aplicación y servicios.</p>

    <h2 class="mt-4">1. Introducción</h2>
    <p>En <strong>La Rosticería</strong>, valoramos su privacidad y estamos comprometidos a proteger la información personal que comparte con nosotros. Esta Política de Privacidad explica cómo recopilamos, utilizamos y protegemos su información cuando utiliza nuestra aplicación y servicios.</p>

    <h2 class="mt-4">2. Información que Recopilamos</h2>
    <p>Recopilamos varios tipos de información en relación con el uso de nuestra aplicación:</p>
    <ul>
      <li><strong>Información Personal:</strong> Datos como nombre, dirección de correo electrónico, número de teléfono y otros detalles similares que nos proporciona al registrarse o utilizar nuestros servicios.</li>
      <li><strong>Información de Uso:</strong> Datos sobre cómo utiliza nuestra aplicación, incluidos los tiempos de acceso, las páginas visitadas y las interacciones con nuestras funciones.</li>
      <li><strong>Información del Dispositivo:</strong> Datos sobre el dispositivo que utiliza para acceder a nuestra aplicación, como la dirección IP, el tipo de dispositivo, el sistema operativo y el navegador.</li>
    </ul>

    <h2 class="mt-4">3. Uso de la Información</h2>
    <p>Utilizamos la información recopilada para varios fines:</p>
    <ul>
      <li><strong>Provisión de Servicios:</strong> Para operar, mantener y mejorar nuestra aplicación y servicios.</li>
      <li><strong>Comunicación:</strong> Para comunicarnos con usted, responder a sus consultas y proporcionarle actualizaciones sobre nuestros servicios.</li>
      <li><strong>Personalización:</strong> Para personalizar su experiencia y mostrarle contenido y anuncios relevantes.</li>
      <li><strong>Análisis:</strong> Para comprender cómo se utiliza nuestra aplicación y mejorarla continuamente.</li>
    </ul>

    <h2 class="mt-4">4. Compartir su Información</h2>
    <p>No compartimos su información personal con terceros, excepto en las siguientes circunstancias:</p>
    <ul>
      <li><strong>Con su Consentimiento:</strong> Cuando usted haya dado su consentimiento explícito para compartir la información.</li>
      <li><strong>Proveedores de Servicios:</strong> Con terceros que nos ayudan a operar nuestra aplicación y proporcionar nuestros servicios, bajo estrictas obligaciones de confidencialidad.</li>
      <li><strong>Obligaciones Legales:</strong> Cuando sea requerido por la ley o para responder a procesos legales válidos.</li>
    </ul>

    <h2 class="mt-4">5. Seguridad de la Información</h2>
    <p>Implementamos medidas de seguridad adecuadas para proteger su información contra accesos no autorizados, alteraciones, divulgaciones o destrucción. Sin embargo, tenga en cuenta que ningún método de transmisión por internet o almacenamiento electrónico es completamente seguro.</p>

    <h2 class="mt-4">6. Retención de Datos</h2>
    <p>Retenemos su información personal solo durante el tiempo necesario para cumplir con los fines para los cuales fue recopilada, incluyendo el cumplimiento de obligaciones legales, contables o de informes.</p>

    <h2 class="mt-4">7. Sus Derechos</h2>
    <p>Usted tiene ciertos derechos respecto a su información personal, incluidos los derechos de acceso, corrección, eliminación y objeción al procesamiento de su información. Para ejercer estos derechos, por favor, póngase en contacto con nosotros.</p>

    <h2 class="mt-4">8. Cambios en esta Política de Privacidad</h2>
    <p>Podemos actualizar esta Política de Privacidad ocasionalmente. Le notificaremos cualquier cambio mediante la publicación de la nueva política en esta página. Se recomienda revisar periódicamente esta política para estar informado sobre cómo protegemos su información.</p>

